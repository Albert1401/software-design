package figures;

public class Line {
    public final double x1;
    public final double y1;
    public final double x2;
    public final double y2;

    public Line(double x1, double y1, double x2, double y2) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
    }
}
